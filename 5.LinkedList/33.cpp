class Solution
{
    public:
    Node *compute(Node *head)
    {
        if(head==NULL or head->next==NULL) return head;
       Node *temp=compute(head->next);
       if(temp->data>head->data) return temp;
       head->next=temp;
       return head;
    }
    
};
   
